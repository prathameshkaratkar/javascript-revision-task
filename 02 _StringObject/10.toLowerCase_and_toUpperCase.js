// ! toLowerCase() method returns a string without any uppercase letters similarily toUpperCase() methods returns a string without any lowercase letters

/*
    Syntax => for lowercase -> string.toLowerCase()
              for uppercase -> string.toUpperCase()
*/

// Example

const greeting = 'hello WORLD'

const strLower = greeting.toLowerCase() 
console.log(strLower) // output => hello world

const strUpper = greeting.toUpperCase()
console.log(strUpper) // output =>  HELLO WORLD
